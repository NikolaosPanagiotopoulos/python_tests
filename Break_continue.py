#σε αυτό το παράδειγμα θα χρησιμοποιήσουμε την break μαζί με το else
print("Θα δώσεις 10 αριθμούς για να υπολογίσω το γινόμενο τους")
print("αν κάποιος αριθμός ειναι 0 τερματίζεται ο υπολογισμός")
ginomeno=1
for i in range(1,11):
    a=int(input("Δώσε τον {}-ο αριθμό: ".format(i)))
    if a==0:
        break
    ginomeno*=a
else:
    print("Δεν εντοπίστηκε καμία μηδενική είσοδος")
print("Το γινόμενο είναι:",ginomeno)
    
