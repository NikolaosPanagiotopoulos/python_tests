#σε αυτό το παράδειγμα θα δημιουργήσουμε σύνολα sets
#Θα αφαιρέσουμε όλα τα στοιχεία με τη μέθοδο clear()
print("----Σύνολο1---")
print("-----------Θα δημιουργήσουμε ενα σύνολο με set()----------------- ")
myset1=set(("a","b","c","d","e","f","g"))
print("Αρχικά: ",myset1,"Πλήθος Στοιχειων: ",len(myset1))
myset1.clear()
print("εκτελέσαμε τo clear()",myset1,"Πλήθος στοιχείων ",len(myset1))
myset1.add("Hello")
print("Προσθέσαμε ένα ακόμη στοιχείο ",myset1,"Πλήθος στοιχειων ",len(myset1))






