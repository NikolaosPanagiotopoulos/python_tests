#σε αυτό το παράδειγμα θα δημιουργήσουμε μία συνάρτηση
#sum_range() υπολογίζει το άθροισμα των ακεραίων που βρίσκονται
#ανάμεσα σε δύο άλλους ακεραίους πχ a=1 και b=15.
#αυτές οι τιμές στα ορίσματα είναι προκαθορισμένες
def sum_range(a=1,b=15):
    athr=0
    for i in range(a+1,b):
        athr+=i
    return athr
print(sum_range(4))
print(sum_range(b=4))
