#σε αυτό το παράδειγμα θα δημιουργήσουμε μία συνάρτηση
#sum_range() υπολογίζει το άθροισμα των ακεραίων που βρίσκονται
#ανάμεσα σε δύο άλλους ακεραίους πχ a=1 και b=15.
def sum_range(a=1,b=15):
    athr=0
    for i in range(a+1,b):
        athr+=i
    return athr
print(sum_range(5,100))
print(sum_range(20,40))
#επειδή εχουμε βάλει ορίσματα προκαθορισμένες τιμές παραμέτρων
#δεν βγάζει μήνυμα σφάλματος όταν βάζουμε λιγότερες παραμέτρους
print(sum_range())
print(sum_range(4))
