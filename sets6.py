#Θα αφαιρέσουμε στοιχεία με remove() και discard
#το remove()εμφανίζει σφάλμα
#οταν πάμε να αφαιρέσουμε ενα στοιχείο που δεν υπάρχει

print("----Σύνολο1---")
print("-----------Θα δημιουργήσουμε ενα σύνολο με set()----------------- ")
myset1=set(("a","b","c","d","e","f","g"))
print(myset1)
myset1.discard("w")
print("εκτελέσαμε την εντολή discard")
print(myset1)
myset1.remove("w")
print("εκτελέσαμε την έντολή remove")
print(myset1)



