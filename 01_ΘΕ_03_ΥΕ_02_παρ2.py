# type casting str->int
num=input("πληκτρολογήστε έναν ακέραιο αριθμό:")
num=int(num)
print(num)
print("Ο τύπος δεδομένων του αριθμού είναι:", type(num))
#type casting str->float
num2=input("πληκτρολογήστε έναν δεκαδικό αριθμό:")
num2=float(num2)
print(num2)
print("Ο τύπος δεδομένων του αριθμού ειναι:",type(num2))
