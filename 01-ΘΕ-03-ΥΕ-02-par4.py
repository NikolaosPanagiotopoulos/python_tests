#άρτιος ή περιττός αριθμός
"""Σε αυτό το παράδειγμα
θα υπολογίσουμε το ακέραιο
υπόλοιπο διαιρεσης με το 2 ενός αριθμού
μετά θα εμφανίσει στην οθόνη
αν αυτός ο αριθμός
ειναι άρτιος ή περιττός"""
num = int(input("Δώσε εναν αριθμό: "))
mod=num%2
if mod>0:
    print("ο αριθμός ",num,"είναι περιττός")
else:
    print("ο αριθμός",num,"ειναι άρτιος")
