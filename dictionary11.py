#σε αυτό το παράδειγμα θα δημιουργήσουμε λεξικά        
#Θα διαγράψουμε στοιχεία με τη μέθοδο pop()
print("----Πρώτο Λεξικό---")
students = {
    "names":["Άννα","Γιώργος","Βασιλική","Δημήτρης"],
    "age":[10,13,34,56]
    }
print('Εμφανίζουμε τα στοιχεία του λεξικού:')
for x in students.items():
    print(x)
print('\n')
students.pop('age')
print('Εμφανίζουμε τα στοιχεία του λεξικού μετά την διαγραφή με pop()')
for x in students.items():
    print(x)
print('\n')    
